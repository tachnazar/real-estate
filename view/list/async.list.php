<script>
$(document).ready(function() {
  $favorite_toggle = $('.star-toggle');
  $favorite_toggle.on('click', function() {
    $(this).children('i').toggleClass('far fas');
    $(this).toggleClass('text-dark text-success');
  });

  $('.star-rating').each(function (idx, ele) {
    var $this = $(ele);
    var readonly = $this.data('readonly');
    if (typeof (readonly) == 'undefined') {
      readonly = false;
    }
    var theme = $this.data('theme');
    if (typeof (theme) == 'undefined') {
      theme = 'css-stars';
    }
    $this.barrating({
      'theme': theme,
      'readonly': readonly
    });
  });
});
</script>